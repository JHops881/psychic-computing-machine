  void drawVisibleWalls(shaderObj::Shader& shader, models::WallModel& wall) {
    wall.select();
    for (int i = 0; i < 572; i++) {
      if (visibleWalls[i] != 0) {

        float xPos = (i+31) % 31;
        xPos -= 15.0f;

        
        float yPos = (i+1) / 31;
        yPos = 16.0f - yPos;
        yPos -= 8.0f;

        glm::vec3 pos = glm::vec3(xPos, yPos, 0.0f);
        
        glm::mat4 modelMatrix = glm::mat4(1.0f);        
        modelMatrix = glm::translate(modelMatrix, pos);                 
        shader.setMat4fv("model", modelMatrix);

        shader.use();
        glDrawElements(GL_TRIANGLES, 30, GL_UNSIGNED_INT, 0);  
      }
    }
  }

