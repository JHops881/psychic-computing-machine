  void DrawVisibleWalls(shader_obj::Shader& shader, models::WallModel& wall) {
    wall.Select();
    for (int i = 0; i < 572; i++) {
      if (visible_walls[i] != 0) {

        float xpos = (i+31) % 31;
        xpos -= 15.0f;

        
        float ypos = (i+1) / 31;
        ypos = 16.0f - ypos;
        ypos -= 8.0f;

        glm::vec3 pos = glm::vec3(xpos, ypos, 0.0f);
        
        glm::mat4 model_mat = glm::mat4(1.0f);        
        model_mat = glm::translate(model_mat, pos);                 
        shader.SetMat4fv("model", model_mat);

        shader.use();
        glDrawElements(GL_TRIANGLES, 30, GL_UNSIGNED_INT, 0);  
      }
    }
  }

